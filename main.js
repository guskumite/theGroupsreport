function thePrinter(data) {
	 let groupHTML = document.querySelector('.group');
	 let html = "";
	 
	 for (grupo of data) {
		html += `
		<h2 id="${grupo.color}"> ${grupo.title}</h2>		
		`;
		html += `<div class="subgroups"> <div class="subgroup">
		`;
		for (subgrupo of grupo.subGroups) {
			html += `
			<h2 id="${subgrupo.title}">${subgrupo.title}</h2>
			`;
			html += `<div class="participants">
			`;
			for (participante of subgrupo.participants) {
				html += `
				<div class="participant">
				<h2>${participante.name} <br> ${participante.email} </h2>
				</div>
				`;
			}
			html += `</div>`
		}
	 }
	 groupHTML.innerHTML = html;

	 for (i = 0; i <= data.length - 1; i++) {	
         document.getElementById(`${data[i].color}`).style.color = `${data[i].color}`;
		 for (j = 0; j <= data[i].subGroups.length - 1; j++) {
			document.getElementById(`${data[i].subGroups[j].title}`).style.color = `${data[i].color}`;
		 }
	 }
	 
}

thePrinter(data);